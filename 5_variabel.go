package main

import "fmt"

func main() {
	/*
		Variabel pada Go :
		- Hanya bisa menyimpan tipe data yang sama
		- Untuk membuat variabel, kita harus menggunakan kata kunci var, lalu diikuti nama variabel dan tipe datanya
	*/
	var name string

	name = "Fais Nasrullah"
	fmt.Println(name)

	name = "Eko Sungkowo"
	fmt.Println(name)
}
